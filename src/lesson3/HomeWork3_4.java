public class HomeWork3_4 {

    public static void main(String[] args) {

        int budgetMonth = 9500;
        double year = 12;
        double month = 0;
        double timeSpent = 0.0;

        for ( int i = 9500 ; i <= 200000 ; i += budgetMonth){
            if ( i > -1000){
                month = month + 1;
            }




        } timeSpent = month / year;
        System.out.println("Сколько потребуется лет :" + timeSpent);








    }


}
